# 12 - Faça um programa para calcular o valor final da conta de um restaurante sabendo-se que deveserá ser acrescida a conta 10% do valor final

valor_conta = input('Digite o valor final da conta:')

valor_conta = int(valor_conta)
valor_final_conta = valor_conta * 1.1

print('O valor final da conta e de:', valor_final_conta)